--
-- Triggers `service_log`
--
DELIMITER $$
CREATE TRIGGER `after_service_log_update` AFTER UPDATE ON `service_log` FOR EACH ROW BEGIN
    -- Log status changes
    IF OLD.status != NEW.status THEN
        INSERT INTO service_log_history (service_id, changed_field, old_value, new_value, changed_by)
        VALUES (NEW.service_id, 'status', OLD.status, NEW.status, NEW.acknowledged_by);
    END IF;
    
    -- Log solution updates
    IF OLD.solution != NEW.solution THEN
        INSERT INTO service_log_history (service_id, changed_field, old_value, new_value, changed_by)
        VALUES (NEW.service_id, 'solution', OLD.solution, NEW.solution, NEW.acknowledged_by);
    END IF;
    
    -- Log return date updates
    IF OLD.return_date != NEW.return_date THEN
        INSERT INTO service_log_history (service_id, changed_field, old_value, old_value, changed_by)
        VALUES (NEW.service_id, 'return_date', 
                IFNULL(OLD.return_date, 'NULL'), 
                IFNULL(NEW.return_date, 'NULL'), 
                NEW.acknowledged_by);
    END IF;
END
$$
DELIMITER ;
DELIMITER $$
CREATE TRIGGER `before_service_log_insert` BEFORE INSERT ON `service_log` FOR EACH ROW BEGIN
    DECLARE asset_warranty_date DATE;
    
    -- Get warranty expiry date for the asset
    SELECT warranty_expiry INTO asset_warranty_date 
    FROM asset 
    WHERE asset_id = NEW.asset_id;
    
    -- Set warranty status based on current date
    IF asset_warranty_date IS NOT NULL THEN
        IF NEW.service_date <= asset_warranty_date THEN
            SET NEW.warranty_status = 'In Warranty';
        ELSE
            SET NEW.warranty_status = 'Out of Warranty';
        END IF;
    END IF;
END
$$
DELIMITER ;

